    private void entrarjLabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_entrarjLabelMouseClicked
        // TODO add your handling code here:
        //resgata a senha do banco de acordo com o usu√°rio digitado e guarda na variavel recebe_senha para verifica√ß√£o.
        recebe_senha = null;
        
        try {
            Usuarios oUsuario = new Usuarios();
            ResultSet rs;
            oUsuario.setNome_usuario(usuariojTextField.getText().trim().toUpperCase());
            rs = oUsuario.getConsultar(conecta);
            while (rs.next()) {
                recebe_senha = rs.getString("SENHA");
                usuario = rs.getString("NOME_USUARIO");
                codigo_usuario_pessoa = rs.getInt("CODIGO_PESSOA_USUARIO");
                nome_pessoa = rs.getString("NOME_PESSOA");
                nivel_acesso = rs.getString("NIVEL_ACESSO");
            }
        } catch (SQLException ex) {
            Logger.getLogger(jFLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
//==============================================================================================================================
        
        //pega a senha do campo senha e criptografa
        // CRIPTOGRAFA SENHA
        senha = new String(senhajPasswordField.getPassword()); //CRIA UMA VARIAVEL TIPO STRING E PEGA A PASSWORD
        EncriptaSenha.encripta(senha);                           //ENVIA A SENHA DA VARIAVEL SENHA PARA A CLASSE ENCRIPTA SENHA E REALIZAA CRIPTOGRAFIA
        System.out.println(EncriptaSenha.encripta(senha));
        System.out.println(recebe_senha);
        
        System.out.println("\n");
        System.out.println(codigo_usuario_pessoa);
        System.out.println(nome_pessoa);
        System.out.println(usuario);
        System.out.println(nivel_acesso);

 //==================================================================================================================================       
        if(senhajPasswordField.getText().length() == 0){
            JOptionPane.showMessageDialog(null, "Aten√ß√£o!\n<html><body>O campo <b>\"Senha\" </b>√© obrigat√≥rio</body></html>", 
            "Mensagem do sistema", JOptionPane.WARNING_MESSAGE);
            
        }else if (usuariojTextField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Aten√ß√£o!\n<html><body>O campo <b>\"Usu√°rio\" </b>√© obrigat√≥rio</body></html>", 
            "Mensagem do sistema", JOptionPane.WARNING_MESSAGE);
            
        }else if(!EncriptaSenha.encripta(senha).equals(recebe_senha)){
            JOptionPane.showMessageDialog(null, "A senha ou o usu√°rio n√£o conferem");
            usuariojTextField.setText("");
            senhajPasswordField.setText("");
            usuariojTextField.grabFocus();
        }else{
            
            try {
                    jFHome oFormHome = new jFHome();
                    
                    oFormHome.codigo_pessoa_usuario = codigo_usuario_pessoa;
                    oFormHome.nome_usuario = nome_pessoa;
                    oFormHome.usuario = usuario;
                    oFormHome.nivel_acesso = nivel_acesso;
                    
                    oFormHome.setLocationRelativeTo(null);
                    oFormHome.pack();
                    oFormHome.setVisible(true);
                    oFormHome.setExtendedState(JFrame.MAXIMIZED_BOTH);
                    this.dispose();

            } catch (ClassNotFoundException | InstantiationException | IllegalAccessException e) {
                JOptionPane.showMessageDialog(null, "Erro ao realizar o login\n"+e, "Mensagem do sistema", JOptionPane.ERROR_MESSAGE);
            }
 
        }
    }//GEN-LAST:event_entrarjLabelMouseClicked

