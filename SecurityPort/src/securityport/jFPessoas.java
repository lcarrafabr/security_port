    public void getFieldPessoas(int codigo_click_pessoa) throws ParseException{

        Pessoas oPessoa = new Pessoas();
        ResultSet rs;
        oPessoa.setCodigo_pessoa(codigo_click_pessoa);

        try {
            rs = oPessoa.getConsultar(conecta);
            rs.next();

            //Pegar campos
            nomePessoajTextField.setText(rs.getString("NOME_PESSOA"));
            dataCadastrojFormattedTextField.setValue(Funcoes.getDataFormat(rs.getDate("DATA_CADASTRO")));
            statusjComboBox.setSelectedItem(rs.getString("STATUS"));
        } catch (SQLException ex) {
            Logger.getLogger(Pessoas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
