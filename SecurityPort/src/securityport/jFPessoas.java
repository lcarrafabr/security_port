    private void salvarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarjButtonActionPerformed
        // TODO add your handling code here:
        if(nomePessoajTextField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Atenção!\nInsira um nome", "Mensagem do sistema", JOptionPane.INFORMATION_MESSAGE);
        }else{
            try {
                buscarjButton.setVisible(true);
                novojButton.setVisible(true);
                editarjButton.setVisible(false);
                salvarjButton.setVisible(false);
                salvarMaisjButton.setVisible(false);
                alterarjButton.setVisible(false);
                excluirjButton.setVisible(false);
                cancelarjButton.setVisible(false);
                
                Pessoas oPessoa = new Pessoas();
                
                oPessoa.setNome_pessoa(nomePessoajTextField.getText().trim().toUpperCase());
                oPessoa.setStatus(statusjComboBox.getSelectedItem().toString());
                oPessoa.setData_cadastro(Funcoes.getDataFormatBR(dataCadastrojFormattedTextField.getText()));
                oPessoa.setCadastrar();           
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro ao cadastrar\n"+e, "Mensagem do sistema", JOptionPane.ERROR_MESSAGE);
            }
            try {
            getGradePessoa();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao carregar a grade Pessoa\n"+e, "Mensagem do sistema", JOptionPane.ERROR_MESSAGE);
        }
        }
    }//GEN-LAST:event_salvarjButtonActionPerformed

