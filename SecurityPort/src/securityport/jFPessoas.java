    private void alterarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterarjButtonActionPerformed
        // TODO add your handling code here:
        if(nomePessoajTextField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Insira um nome co campo Pessoa", "Mensagem do sistema", JOptionPane.INFORMATION_MESSAGE);
                        
        }else{
            try {
                buscarjButton.setVisible(true);
                novojButton.setVisible(true);
                editarjButton.setVisible(false);
                salvarjButton.setVisible(false);
                salvarMaisjButton.setVisible(false);
                alterarjButton.setVisible(false);
                excluirjButton.setVisible(false);
                cancelarjButton.setVisible(false);
                
                Pessoas oPessoa = new Pessoas();
                
                oPessoa.setCodigo_pessoa(codigo_click_pessoa);
                
                oPessoa.setNome_pessoa(nomePessoajTextField.getText().trim().toUpperCase());
                oPessoa.setData_cadastro(Funcoes.getDataFormatBR(dataCadastrojFormattedTextField.getText()));
                oPessoa.setStatus(statusjComboBox.getSelectedItem().toString());
                oPessoa.setAlterar(conecta);
            } catch (Exception e) {
            }
            try {
                getGradePessoa();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro ao carregar a grade\n"+e, "Mensagen do sistema", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_alterarjButtonActionPerformed

