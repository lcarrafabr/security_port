    public void getGradeTipoAcessoVisitante() throws ParseException {
        //Consultar Tipo acesso visitantes
        TipoAcessoVisitantes oTipoAcesso = new TipoAcessoVisitantes();

        ResultSet resultSet = null;

        String[] colunasTabela = new String[]{"#Id", "Nome tipo acesso"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        gradeTipoAcessoVisitantejTable.setModel(modeloTabela);
        gradeTipoAcessoVisitantejTable.getColumnModel().getColumn(0).setPreferredWidth(10);
        gradeTipoAcessoVisitantejTable.getColumnModel().getColumn(1).setPreferredWidth(350);

//        gradePessoajTable.setDefaultRenderer(Object.class, new ColorRenderer()); // DEIXA A GRADE ZEBRADA
        try {
            resultSet = oTipoAcesso.getConsultar(conecta);
            if (resultSet.getRow() == 1) {
                resultSet.first();
            }

        } catch (SQLException ex) {
            Logger.getLogger(Pessoas.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            while (resultSet.next()) {

                modeloTabela.addRow(new String[]{
                    Funcoes.getCodigoFormat(resultSet.getInt("CODIGO_TIPO_ACESSO_VISITANTE")),
                    resultSet.getString("NOME_ACESSO")

                });

            }

        } catch (SQLException ex) {
            Logger.getLogger(Pessoas.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    public void getFieldTipoAcesso(int codigo_click_acesso) throws ParseException {

        TipoAcessoVisitantes oTipoAcesso = new TipoAcessoVisitantes();
        ResultSet rs;
        oTipoAcesso.setCodigo_tipo_acesso(codigo_click_acesso);

        try {
            rs = oTipoAcesso.getConsultar(conecta);
            rs.next();

            //Pegar campos
            nomeTipoAcessojTextField.setText(rs.getString("NOME_ACESSO"));
            informacaojTextArea.setText(rs.getString("INFORMACAO_ACESSO"));
            corjTextField.setText(rs.getString("COR"));

            String cor_rgb = (corjTextField.getText());

            String tar = cor_rgb;
            String[] teste = tar.split(",");

            Color color = new Color(Integer.parseInt(teste[0]), Integer.parseInt(teste[1]), Integer.parseInt(teste[2]));

            painelDeCorjPanel.setBackground(color);

//            painelDeCorjPanel.setBackground(cor_rgb);
        } catch (SQLException ex) {
            Logger.getLogger(jFTipoAcessoVisitante.class.getName()).log(Level.SEVERE, null, ex);
        }

    }


