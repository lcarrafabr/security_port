    private void paletaDeCoresjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_paletaDeCoresjButtonActionPerformed
        // TODO add your handling code here:
        Color color = JColorChooser.showDialog(null, "Escolha a cor", Color.WHITE);

        if (color == null) {
            color = (Color.WHITE);
        } else {
            painelDeCorjPanel.setBackground(color);
            rgb = color;
            corjTextField.setText(rgb.toString()); // PEGA A COR E TRANSFORMA E MSTRING

            String rgbConvertido = corjTextField.getText().replaceAll("java.awt.Color", "").replace("[", "").replaceAll("r=", "").
                    replaceAll("g=", "").replaceAll("b=", "").replace("]", ""); // RETIRA TODOS OS CARACTERES E MANTEM APENAS A COR RGB
            corjTextField.setText("");
            corjTextField.setText(rgbConvertido);
        }
    }//GEN-LAST:event_paletaDeCoresjButtonActionPerformed

