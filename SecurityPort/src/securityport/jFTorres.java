    public void getGradeTorres() throws ParseException{
       //Consultar Torres
        Torres oTorre = new Torres();


        ResultSet resultSet = null;

        String[] colunasTabela = new String[]{"#Id", "Torre"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        gradeTorrejTable.setModel(modeloTabela);
        gradeTorrejTable.getColumnModel().getColumn(0).setPreferredWidth(10);
        gradeTorrejTable.getColumnModel().getColumn(1).setPreferredWidth(200);

//        gradePessoajTable.setDefaultRenderer(Object.class, new ColorRenderer()); // DEIXA A GRADE ZEBRADA
        

try {
            resultSet = oTorre.getConsultar(conecta);
            if (resultSet.getRow() == 1){
             resultSet.first();
            }


        } catch (SQLException ex) {
            Logger.getLogger(Cargos.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            while (resultSet.next()) {

                modeloTabela.addRow(new String[]{
                    Funcoes.getCodigoFormat(resultSet.getInt("CODIGO_TORRE")),
                    resultSet.getString("NOME_TORRE")
                    
                });

            }

        } catch (SQLException ex) {
            Logger.getLogger(Cargos.class.getName()).log(Level.SEVERE, null, ex);
        }

           }
    
    public void getFieldTelefones(int codigo_click_torre) throws ParseException{

        Torres oTorre = new Torres();
        ResultSet rs;
        oTorre.setCodigo_torre(codigo_click_torre);

        try {
            rs = oTorre.getConsultar(conecta);
            rs.next();

            //Pegar campos
            nomeTorrejTextField.setText(rs.getString("NOME_TORRE"));
        } catch (SQLException ex) {
            Logger.getLogger(jFTorres.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao preecher campos\n"+ex, "Mensagem do sistema", JOptionPane.ERROR_MESSAGE);
        }
    }
    

