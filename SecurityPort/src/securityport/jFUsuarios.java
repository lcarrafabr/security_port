    public void getGradeusuarios() throws ParseException{
       //Consultar Usuario
        Usuarios oUsuario = new Usuarios();

        ResultSet resultSet = null;

        String[] colunasTabela = new String[]{"#Id", "Usuário", "Nivel acesso"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        gradeusuariosjTable.setModel(modeloTabela);
        gradeusuariosjTable.getColumnModel().getColumn(0).setPreferredWidth(10);
        gradeusuariosjTable.getColumnModel().getColumn(1).setPreferredWidth(180);
        gradeusuariosjTable.getColumnModel().getColumn(2).setPreferredWidth(100);

//        gradePessoajTable.setDefaultRenderer(Object.class, new ColorRenderer()); // DEIXA A GRADE ZEBRADA

try {
            oUsuario.setCodigo_pessoa_usuario(recebe_codigo_pessoa);
            resultSet = oUsuario.getConsultar(conecta);
            if (resultSet.getRow() == 1){
             resultSet.first();
            }


        } catch (SQLException ex) {
            Logger.getLogger(Pessoas.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            while (resultSet.next()) {

                modeloTabela.addRow(new String[]{
                    Funcoes.getCodigoFormat(resultSet.getInt("CODIGO_PESSOA_USUARIO")),
                    resultSet.getString("NOME_USUARIO"),
                    resultSet.getString("NIVEL_ACESSO")
                    
                });

            }

        } catch (SQLException ex) {
            Logger.getLogger(Pessoas.class.getName()).log(Level.SEVERE, null, ex);
        }

           }
    
    public void getFieldUsuarios(int codigo_click_usuario) throws ParseException{

        Usuarios oUsuario = new Usuarios();
        ResultSet rs;
        oUsuario.setCodigo_pessoa_usuario(codigo_click_usuario);

        try {
            rs = oUsuario.getConsultar(conecta);
            rs.next();

            //Pegar campos
            nomeUsuariojTextField.setText(rs.getString("NOME_USUARIO"));
//            senhajPasswordField.setText(rs.getString("SENHA"));
//            confirmarSenhajPasswordField.setText(rs.getString("SENHA"));
            nivelAcessojComboBox.setSelectedItem(rs.getString("NIVEL_ACESSO"));
        } catch (SQLException ex) {
            Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao preecher campos\n"+ex, "Mensagem do sistema", JOptionPane.ERROR_MESSAGE);
        }
    }
    
