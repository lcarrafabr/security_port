    private void alterarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterarjButtonActionPerformed
        // TODO add your handling code here:
        if(nomeUsuariojTextField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Atenção!\n<html><body>O campo <b>\"Nome Usuário\" </b>é obrigatório</body></html>", 
            "Mensagem do sistema", JOptionPane.WARNING_MESSAGE);
            
        }else if (senhajPasswordField.getText().length() == 0){
            JOptionPane.showMessageDialog(null, "Atenção!\n<html><body>O campo <b>\"SENHA\" </b>é obrigatório</body></html>", 
            "Mensagem do sistema", JOptionPane.WARNING_MESSAGE);
            
        }else if (repetirSenhajPasswordField.getText().length() == 0){
            JOptionPane.showMessageDialog(null, "Atenção!\n<html><body>O campo <b>\"Repetir senha\" </b>é obrigatório</body></html>", 
            "Mensagem do sistema", JOptionPane.WARNING_MESSAGE);
            
        }else if (senhajPasswordField.getText().length() != repetirSenhajPasswordField.getText().length()){
            JOptionPane.showMessageDialog(null, "Atenção!\nAs senhas não conferem", 
            "Mensagem do sistema", JOptionPane.WARNING_MESSAGE);
            
        }else if(nivelAcessojComboBox.getSelectedItem().equals("SELECIONE")){
            JOptionPane.showMessageDialog(null, "Atenção!\n<html><body>O campo <b>\"Hierarquia\" </b>é obrigatório</body></html>", 
            "Mensagem do sistema", JOptionPane.WARNING_MESSAGE);
            
        }else{  
            try {
                buscarjButton.setVisible(true);
                novojButton.setVisible(true);
                editarjButton.setVisible(false);
                salvarjButton.setVisible(false);
                alterarjButton.setVisible(false);
                excluirjButton.setVisible(false);
                cancelarjButton.setVisible(false);
                
                Usuarios oUsuarios = new Usuarios();             
                
                oUsuarios.setCodigo_pessoa_usuario(recebe_codigo_pessoa);
                oUsuarios.setNome_usuario(nomeUsuariojTextField.getText().trim().toUpperCase());
                oUsuarios.setNivel_acesso(nivelAcessojComboBox.getSelectedItem().toString());
                
                
                // CRIPTOGRAFA SENHA
                String senha = new String(senhajPasswordField.getPassword()); //CRIA UMA VARIAVEL TIPO STRING E PEGA A PASSWORD
                EncriptaSenha.encripta(senha);                           //ENVIA A SENHA DA VARIAVEL SENHA PARA A CLASSE ENCRIPTA SENHA E REALIZAA CRIPTOGRAFIA
                System.out.println(EncriptaSenha.encripta(senha));
                oUsuarios.setSenha(EncriptaSenha.encripta(senha));       //ENVIA A SENHA CRIPTOGRAFADA PARA A CLASSE
                oUsuarios.setAlterar(conecta);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro ao cadastrar o usuário\n"+e, "mensagem do sistema", JOptionPane.ERROR_MESSAGE);
            }
            buscarjButtonActionPerformed(evt);
        }
    }//GEN-LAST:event_alterarjButtonActionPerformed

    private void excluirjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirjButtonActionPerformed
        // TODO add your handling code here:
        Object[] options = {"Sim", "Não"};
        int sd =
                JOptionPane.showOptionDialog(null, "Deseja realmente EXCLUIR esse registro?", "Confirmação!", JOptionPane.DEFAULT_OPTION,
                JOptionPane.WARNING_MESSAGE, null, options, options[0]);

        if (sd != 0) {
            return;
        }
        
        try {
            cancelarjButtonActionPerformed(evt);
        
            Usuarios oUsuario = new Usuarios();
        
            oUsuario.setCodigo_pessoa_usuario(codigo_click_usuario);
            oUsuario.setExcluir(conecta);
            
            nomeUsuariojTextField.setText("");
            senhajPasswordField.setText("");
            repetirSenhajPasswordField.setText("");
            nivelAcessojComboBox.setSelectedItem("SELECIONE");
        
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao excluir registro\n"+e, "Mensagem do sistema", JOptionPane.ERROR_MESSAGE); 
        }
        buscarjButtonActionPerformed(evt);
    }//GEN-LAST:event_excluirjButtonActionPerformed

