    private void salvarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarjButtonActionPerformed
        // TODO add your handling code here:

        if (nomeVisitantejTextField.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Atenção!\n"
                    + "Selecione um visitante no botão consultar.",
                    "Mensagem do sistema", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                buscarjButton.setVisible(true);
                novojButton.setVisible(true);
                editarjButton.setVisible(false);
                salvarjButton.setVisible(false);
                salvarMaisjButton.setVisible(false);
                alterarjButton.setVisible(false);
                excluirjButton.setVisible(false);
                cancelarjButton.setVisible(false);

                Visitantes oVisitante = new Visitantes();

                hora_atual = Funcoes.getHoraAtual();

                try {
                    data_atual = Funcoes.getNowBR();
                } catch (ParseException ex) {
                    Logger.getLogger(jFVisitantes.class.getName()).log(Level.SEVERE, null, ex);
                }

                oVisitante.setCodigo_cadastro_pessoa_visitante(recebe_codigo_visitante);
                System.out.println("Codigo_visitante: " + recebe_codigo_visitante);
                oVisitante.setCodigo_pessoa_usuario(recebe_codigo_usuario);
                oVisitante.setData_visita(Funcoes.getDataFormatBR(data_atual));
                oVisitante.setHora_visita(hora_atual);
                oVisitante.setLocal_da_visita(localDaVisitajTextArea.getText().trim());
                oVisitante.setCadastrar();
                

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro ao cadastrar entrada visitante\n" + e,
                        "Mensagem do sistema", JOptionPane.ERROR_MESSAGE);
                System.err.println(e);
            }
            
            buscarjButtonActionPerformed(evt);

        }


    }//GEN-LAST:event_salvarjButtonActionPerformed

