    public void getGradeVisitante() throws ParseException {
        //Consultar pedidos
        Visitantes oVisitante = new Visitantes();

        ResultSet resultSet = null;

        String[] colunasTabela = new String[]{"#Id", "Visitante", "Data visita", "Usuario do sistema"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        gradeHistoricoVisitajTable.setModel(modeloTabela);
        gradeHistoricoVisitajTable.getColumnModel().getColumn(0).setPreferredWidth(10);
        gradeHistoricoVisitajTable.getColumnModel().getColumn(1).setPreferredWidth(300);
        gradeHistoricoVisitajTable.getColumnModel().getColumn(2).setPreferredWidth(80);
        gradeHistoricoVisitajTable.getColumnModel().getColumn(3).setPreferredWidth(100);

//        gradePessoajTable.setDefaultRenderer(Object.class, new ColorRenderer()); // DEIXA A GRADE ZEBRADA
        
        if(recebe_codigo_visitante != -1){
            oVisitante.setCodigo_cadastro_pessoa_visitante(recebe_codigo_visitante);
        }
        
        try {
            //pega o status e seta na filtro da grade Pessoa
            resultSet = oVisitante.getConsultar(conecta);
            if (resultSet.getRow() == 1) {
                resultSet.first();
            }

        } catch (SQLException ex) {
            Logger.getLogger(Pessoas.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            while (resultSet.next()) {

                //Formatar data
                Date cadastro = resultSet.getDate("DATA_VISITA");

                String dataCadastro;
                if (cadastro != null) {
                    SimpleDateFormat formatoDataLanc = new SimpleDateFormat("dd/MM/yyyy");
                    dataCadastro = formatoDataLanc.format(cadastro);
                } else {
                    dataCadastro = "";
                }
                //Fim formatação

                modeloTabela.addRow(new String[]{
                    Funcoes.getCodigoFormat(resultSet.getInt("CODIGO_CADASTRO_VISITA")),
                    resultSet.getString("NOME_VISITANTE"),
                    dataCadastro.concat(" - ").concat(resultSet.getString("HORA_VISITA")),
                    resultSet.getString("NOME_USUARIO")

                });

            }

        } catch (SQLException ex) {
            Logger.getLogger(Pessoas.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    
    public void getFieldVisitante(int codigo_click_visitor) throws ParseException {

        Visitantes oVititante = new Visitantes();
        ResultSet rs;
        oVititante.setCodigo_cadastro_visita(codigo_click_visitor);

        try {
            rs = oVititante.getConsultar(conecta);
            rs.next();

            //Pegar campos
            nomeVisitantejTextField.setText(rs.getString("NOME_VISITANTE"));
            rgjTextField.setText(rs.getString("RG"));
            localDaVisitajTextArea.setText(rs.getString("LOCAL_DA_VISITA"));
            observacaoVisitantejTextArea.setText(rs.getString("OBSERVACAO"));
            infoAcessojTextArea.setText(rs.getString("INFORMACAO_ACESSO"));
            recebe_cor = rs.getString("COR");
            recebe_info_acesso = rs.getString("INFORMACAO_ACESSO");
            
            
            getCor();
        } catch (SQLException ex) {
            Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao preecher campos\n" + ex, "Mensagem do sistema", JOptionPane.ERROR_MESSAGE);
        }
    }

